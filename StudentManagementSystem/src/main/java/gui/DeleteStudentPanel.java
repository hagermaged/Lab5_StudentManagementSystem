    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String keyword = jTextField1.getText().trim();
        if (keyword.isEmpty()) {
            loadAllStudents();
            return;
        }

        ArrayList<Student> results = (ArrayList<Student>) studentManager.searchStudent(keyword);
        updateTable(results);
        
        if (results.isEmpty()) {
            JOptionPane.showMessageDialog(this, "No students found!", "Information", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
         int selectedRow = jTable1.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Please select a student to delete!", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }

        int studentId = (int) tableModel.getValueAt(selectedRow, 0);
        String studentName = (String) tableModel.getValueAt(selectedRow, 1);

        int confirm = JOptionPane.showConfirmDialog(this,
            "Are you sure you want to delete student:\n" +
            "ID: " + studentId + "\n" +
            "Name: " + studentName + "?",
            "Confirm Deletion",
            JOptionPane.YES_NO_OPTION,
            JOptionPane.WARNING_MESSAGE);

        if (confirm == JOptionPane.YES_OPTION) {
            String result = studentManager.deleteStudent(studentId);
            JOptionPane.showMessageDialog(this, result, "Deletion Result", JOptionPane.INFORMATION_MESSAGE);
            
            if (result.contains("successfully")) {
                loadAllStudents(); // Refresh the table
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

